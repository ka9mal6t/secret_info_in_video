    private static int[] getRGB(int pixel) {
        int[] rgb = new int[3];
        rgb[0] = (pixel >> 16) & 0xff; // red
        rgb[1] = (pixel >> 8) & 0xff;  // green
        rgb[2] = pixel & 0xff;         // blue
        return rgb;
    }

    private static int getRGBValue(int[] rgb) {
        return (rgb[0] << 16) | (rgb[1] << 8) | rgb[2];
    }

    public static boolean allEncrypt(String directory, String info) {

        List<String> pngFiles = new ArrayList<>();

        info += "01234";

        for (String filename : new File(directory).list()) {
            if (filename.toLowerCase().endsWith(".png")) {
                pngFiles.add(filename);
            }
        }
        if (pngFiles.size() / 2  <  info.length() / 3 + 1){
            return false;
        }

        if (pngFiles.size() > 0 && pngFiles.size() % 2 == 1) {
            pngFiles.remove(pngFiles.size() - 1);
        }

        int count = 1;
        for (int i = 0; i < pngFiles.size(); i += 2) {
            if (info.length() > count * 3) {
                String newInfo = info.substring(((count - 1) * 3), count * 3);
                try {
                    encryptBmp(directory + pngFiles.get(i + 1), directory + pngFiles.get(i), directory + pngFiles.get(i + 1), newInfo);
                } catch (IOException e) {
                    e.printStackTrace();
                }
                count++;
            } else {
                String newInfo = info.substring((count - 1) * 3);
                try {
                    encryptBmp(directory + pngFiles.get(i + 1), directory + pngFiles.get(i), directory + pngFiles.get(i + 1), newInfo);
                } catch (IOException e) {
                    e.printStackTrace();
                }
                break;
            }
        }
        return true;

    }
